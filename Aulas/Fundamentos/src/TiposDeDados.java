public class TiposDeDados {
    public static void main(String[] args) {
        byte v1 = 10; // 8 bits
        short v2 = 10; // 16 bits
        int v3 = 10; // 32 bits
        long v4 = 10L; // 64 bits

        float v5 = 10.0F; // 32 bits
        double v6 = 10.0; // 64 bits

        char v7 = 'A'; // 16 bits
        boolean v8 = true; // 1 bit

        String v9 = "Arthur";
    }
}
